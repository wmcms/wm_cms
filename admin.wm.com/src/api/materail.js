import request from '@/utils/request'

/**
 * 查询列表
 */
export function searchMaterial(query) {
  return request({
    url: '/material/search',
    method: 'post',
    data: query
  })
}
/**
 * 保存
 */
export function saveMaterial(query) {
  return request({
    url: '/material/save',
    method: 'post',
    data: query
  })
}
/**
 * 删除所选
 */
export function removeAt(query) {
  debugger
  return request({
    url: '/material/removeat',
    method: 'post',
    data: query
  })
}
